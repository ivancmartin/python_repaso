def sumar(op1,op2):
	print(f"el resultado de la suma es: {op1+op2}")

def restar(op1,op2):
	print(f"el resultado de la resta es: {op1-op2}")

def multiplicar(op1,op2):
	print(f"el resultado de la multiplicacion es: {op1*op2}")

def dividir(dividendo,divisor):
	print(f"el resultado de la division es: {dividendo//divisor}")

def potencia(base,exponente):
	print(f"el resultado de elevar {base} ^  {exponente} es: {base**exponente}")

def redondear(numero):
	print(f"el numero redondeado es: {round(numero)}")
